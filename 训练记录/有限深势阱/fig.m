clear;clc
% 定义x的范围
x1 = -10:0.01:-1.7;  % 区间 [-10, -1.7)
x2 = -1.7:0.01:1.7;  % 区间 [-1.7, 1.7)
x3 = 1.7:0.01:10;    % 区间 [1.7, 10]

% 定义对应的y值
y1 = 4181.94*exp(6.2717*x1);  % 区间 [-10, -1.7) 的值
y2 = 0.7333*cos(0.8452*x2);  % 区间 [-1.7, 1.7) 的值
y3 = 4181.94*exp(-6.2717*x3);  % 区间 [1.7, 10] 的值

% 绘制分段函数
figure;
hold on;
plot(x1, y1, 'b', 'LineWidth', 2);  % 绘制第一个区间
plot(x2, y2, 'r', 'LineWidth', 2);  % 绘制第二个区间
plot(x3, y3, 'g', 'LineWidth', 2);  % 绘制第三个区间

% 添加标记
plot([-1.7, -1.7], [0, 3], 'k--'); % 添加分界线
plot([1.7, 1.7], [0, 4], 'k--');   % 添加分界线

% 添加图例和标签
xlabel('x');
ylabel('y');
title('分段函数图');
legend('y = 2', 'y = 3', 'y = 4', 'Location', 'NorthWest');
grid on;
hold off;
